/* Static log and atan tables covering the r <= 32 reductions at up
   to FIXED_STATIC_TAB_N - 1 = 11 value limbs read per entry plus
   the extra limb the working precision may read: 33 entries each at
   stride FIXED_STATIC_TAB_N, and EVERY stored limb is a value limb
   (an exact truncation, or one ulp below, of the true entry) --
   unlike the dynamic tables, whose bottom limb is a guard used to
   generate them in place, no guard is stored here: consumers read
   entries by (tab, stride) alone.  Generated by
   dev/notes/harnesses/gen_static_tab.c as the top limbs of a
   dynamic table built one limb deeper; t-tab_static enforces that
   identity.

   Small-n evaluations read these instead of the thread-local
   dynamic tables, so short-lived threads skip the precomputation
   entirely, entries stay adjacent regardless of the precision the
   dynamic tables happen to hold, and the hardcoded small-n routines
   avoid the TLS lookup. */

